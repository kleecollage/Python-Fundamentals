x = 6
y = 2
z = 7

print(f"{x} plus {y} equals to {x + y}")
print(f"{x} less {y} equals to {x - y}")
print(f"{x} times {y} equals to {x * y}")
print(f"{x} divided by {y} equals to {x / y}")

print(f"{z} divided to the floor of {y} equals to {z // y}")
print(f"{z} module of {y} equals to {z % y}")

print(f"{x} squared equals to {x**y}")
print(f"{x} cubed equals to {x**3}")
print(f"Square root of {x} equals to {x**0.5}")
